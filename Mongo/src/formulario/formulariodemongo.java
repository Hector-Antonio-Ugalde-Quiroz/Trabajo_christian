    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        //Primero para establecer la conexion con el mongodb    
        MongoClient mongocliente = new MongoClient("localhost", 27017);
        //Para que establezcamos la conecion con la base de datos que vamos a usar
        MongoDatabase mongoDatabase = mongocliente.getDatabase("NUEVA");
        //para que muestre un mensaje de que se establecio la conexion
        System.out.println("Connect to database successfully");
        //la coleccion que vamos a usar
        MongoCollection<Document> collection = mongoDatabase.getCollection("alumnos");

        //Document condicion = new Document("_id", (txtID.getText()));

        Document findDocument = new Document("_id",new ObjectId(txtID.getText())) ;
        MongoCursor<Document> Cursor = collection.find(findDocument).iterator();

        Document res;
        String acti, comi;
        Document datos;
        while (Cursor.hasNext()) {
            res = Cursor.next();
            txtnombre.setText(res.get("Nombre").toString());
            txtpromedio.setText(res.get("promedio").toString());
            txtcarrera.setText(res.get("carrera").toString());
            datos = (Document) res.get("datos");
            txtdireccion.setText(datos.get("direccion").toString());
            txttelefono.setText(String.valueOf(datos.getInteger("telefono").longValue()));
            txtemail.setText(datos.get("email").toString());
            acti = res.get("actividades").toString();
            acti = acti.replaceAll("[\\s\\[\\](){}]", "");
            String[] docList = acti.split(",");
            txtactividad1.setText(docList[0]);
            txtactividad2.setText(docList[1]);
            txtactividad3.setText(docList[2]);
            comi = res.get("comidas").toString();
            comi = comi.replaceAll("[\\s\\[\\](){}]", "");
            String[] dcList = comi.split(",");
            txtcomida1.setText(dcList[0]);
            txtcomida2.setText(dcList[1]);
            txtcomida3.setText(dcList[2]);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

