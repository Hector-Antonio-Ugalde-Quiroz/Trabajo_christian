    private void btncambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncambiarActionPerformed
        // TODO add your handling code here:
        
        MongoClient mongocliente = new MongoClient("localhost", 27017);
        //Para que establezcamos la conecion con la base de datos que vamos a usar
        MongoDatabase mongoDatabase = mongocliente.getDatabase("NUEVA");
        //para que muestre un mensaje de que se establecio la conexion
        System.out.println("Connect to database successfully");
        //la coleccion que vamos a usar
        MongoCollection<Document> collection = mongoDatabase.getCollection("alumnos");
        ObjectId cod = new ObjectId(txtID.getText());

        //Document condicion = new Document("_id", (txtID.getText()));
        Document findDocument = new Document("_id",cod);
        Double prome = Double.parseDouble(txtpromedio.getText());
        
        Document updateDocument = new Document("$set",
                new Document().append("Nombre", txtnombre.getText())
                .append("promedio", prome)
                .append("carrera", txtcarrera.getText())
                .append("datos", 
                        new Document().append("direccion", txtdireccion.getText())
                        .append("telefono", txttelefono.getText())
                        .append("email",txtemail.getText())
                        ).append("actividades", asList(txtactividad1.getText(),txtactividad2.getText(),txtactividad3.getText()))
                .append("comidas", asList(txtcomida1.getText(),txtcomida2.getText(),txtcomida3.getText())));
        
        collection.findOneAndUpdate(findDocument, updateDocument);
    }//GEN-LAST:event_btncambiarActionPerformed

